class MovieDetails {
  int id;
  String title;
  int releaseYear;
  int duration;
  double rating;
  String imageUrl;
  String details;

  MovieDetails({
    required this.id,
    required this.title,
    required this.releaseYear,
    required this.duration,
    required this.rating,
    required this.imageUrl,
    required this.details,
  });
}
